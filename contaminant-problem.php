            <p>Composting is a rapidly growing sector of the waste management industry worldwide.  Most waste entering landfills contains between 30% to 40% organic matter.  When left in landfills, the organic material decomposes and releases methane (amongst other gases) contributing greatly to climate change.  Harmful leachates are also produced and can pollute watershed’s and aquifers.  The cost of dealing with this is enormous.  Small wonder that more and more jurisdictions around the world are mandating the implementation of composting programs to divert the organic material away from landfill.  The composting process turns the waste organic material into highly beneficial compost that has a host of benefits for soil health, and by extension, plant, animal, and human health.  Applying compost to soils can also help capture atmospheric carbon, helping to mitigate the effects of climate change.  Composting is simply a good thing to do to help reduce our negative impact on the earth.</p>
            <p>Diverting the desirable organics from landfill means diverting a certain amount of garbage with it.  Unfortunately, despite society’s best intentions, the global composting industry is struggling with the volume of physical contaminants entering compost facilities.  It is challenging enough to make good compost from municipally sourced food waste and other organic materials, but when composters have to deal with physical contaminants, there is a whole new set of challenges and operational expenses that never seem to cease.</p>
            <p>The main culprit is the ubiquitous plastic; from food packaging and containers, to every conceivable consumer good, too many compost facilities get it all.  Despite regulations and education campaigns, there is simply too much plastic in the world for it not to end up in people’s compost bins.  Then there is the so-called “bio-degradable” plastic industry.  Sure, some of it is truly compostable, but most of it is not, it seems.  Other contaminants that are too common are textiles and carpets, synthetic clothing, diapers, kitchen utensils, metals, glass and rocks.  Memorable finds have been washing machines, cookers, engine blocks, oil filters, and hydraulic hoses.</p>
            <p>These contaminants all mean one thing to composters: higher operating costs.  This can be due increased wear and tear on equipment and personnel.  It is almost always due to the need to landfill large amounts of compost screening overs that are too contaminated to do anything else with.</p>
            <p>If we can apparently put people on the moon, why can’t we develop effective technology to remove garbage from compost?  This has been BCE’s mission for almost ten years, and we are happy to report that we have been very successful!</p>
